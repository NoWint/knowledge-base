export function parsePEQuestions(): PEQuestion[] {
  const blocks = rawQuestions.split(/\n\n+/)
  const questions: PEQuestion[] = []

  for (const block of blocks) {
    const lines = block.trim().split('\n')
    const questionMatch = lines[0]?.match(/^(\d+)\.\s+(.+)$/)

    if (!questionMatch) continue

    const num = parseInt(questionMatch[1], 10)
    const content = questionMatch[2]

    const moduleNum = Math.floor((num - 1) / 40) + 1
    const categoryNum = getCategoryForQuestion(num)
    const answerMatch = lines.find(l => l.startsWith('答案：'))
    const explanationMatch = lines.find(l => l.startsWith('解析：'))

    const options = lines
      .filter(l => /^[A-D]\./.test(l))
      .map(l => ({
        label: l[0],
        text: l.slice(3).trim(),
      }))

    if (!answerMatch || options.length === 0) continue

    questions.push({
      module: moduleNames[String(moduleNum)] || '未知模块',
      category: categoryNames[String(moduleNum)]?.[categoryNum] || '未知分类',
      questionNumber: num,
      content,
      options,
      answer: answerMatch.slice(3).trim(),
      explanation: explanationMatch?.slice(3).trim() || '',
    })
  }

  return questions
}

function getCategoryForQuestion(questionNum: number): number {
  const moduleNum = Math.floor((questionNum - 1) / 40) + 1
  const numInModule = ((questionNum - 1) % 40) + 1

  const categoryBoundaries: Record<number, number[]> = {
    1: [8, 20, 25, 30, 40],
    2: [8, 18, 28, 36, 40],
    3: [10, 20, 28, 40],
    4: [8, 18, 28, 40],
    5: [10, 20, 28, 40],
  }

  const bounds = categoryBoundaries[moduleNum] || [40]
  for (let i = 0; i < bounds.length; i++) {
    if (numInModule <= bounds[i]) return i + 1
  }
  return bounds.length
}

export function getPEModuleStats() {
  const questions = parsePEQuestions()
  const stats = questions.reduce<Record<string, number>>((acc, q) => {
    acc[q.module] = (acc[q.module] || 0) + 1
    return acc
  }, {})
  return stats
}
